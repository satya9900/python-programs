# defining sets
E = {0, 2, 4, 6, 8};
N = {1, 2, 3, 4, 5};
 
#union E and N
print("Union of E and N :",E | N)
 
#ntersection E and N
print("Intersection of E and N :",E & N)
 
#difference E and N
print("Difference of E and N :",E - N)
 
#symmetric difference E and N
print("Symmetric difference of E and N :",E ^ N)
